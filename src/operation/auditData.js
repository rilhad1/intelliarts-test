export const checkDate = date => {
  if (date) {
    let r = /^(\d{4})-(\d{2})-(\d{2})$/;
    if (r.test(date)) {
      let y = RegExp.$1 * 1;
      let m = RegExp.$2 * 1;
      let d = RegExp.$3 * 1;
      let test = new Date(y, m - 1, d);
      return (
        test.getFullYear() === y &&
        test.getMonth() === m - 1 &&
        test.getDate() === d
      );
    } return false;
  } return false;
};

export const ckeckPrice = price => {
  if (price) {
    if (!isNaN(price) && ((+price) >= 0)) {
      return true;
    } return false;
  } return false;
};

export const checkCurrency = currencyName => {
  if (currencyName) {
    if (currency.filter(item => item === currencyName.toUpperCase()).length > 0) {
      return true;
    } return false;
  } return false;
};

